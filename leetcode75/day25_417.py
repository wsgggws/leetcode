from typing import List


class Solution:
    def pacificAtlantic(self, heights: List[List[int]]) -> List[List[int]]:
        """要求既可流向太平洋也可流向大西洋
        遍历每个坐标，深度优先搜索，如果可搜索到太平洋边界，也可搜索到大西洋边界，则添加
        """
        if len(heights) == 0 or len(heights[0]) == 0:
            raise KeyError
        ans = []
        rows, cols = len(heights), len(heights[0])
        for row in range(rows):
            for col in range(cols):
                border_p = [(0, index) for index in range(cols)] + [(index, 0) for index in range(rows)]
                border_a = [(rows - 1, index) for index in range(cols)] + [(index, cols - 1) for index in range(rows)]
                visit_p = [[False for _ in range(cols)] for _ in range(rows)]
                visit_a = [[False for _ in range(cols)] for _ in range(rows)]
                if self.dfs(row, col, rows, cols, heights, visit_p, border_p) and self.dfs(
                    row, col, rows, cols, heights, visit_a, border_a
                ):
                    ans.append([row, col])
        return ans

    def dfs(self, row, col, rows, cols, heights, visit, border) -> bool:
        if (row, col) in border:
            return True
        visit[row][col] = True
        can_steps = []
        for direct in [(-1, 0), (0, -1), (1, 0), (0, 1)]:
            nrow, ncol = row + direct[0], col + direct[1]
            if (
                0 <= nrow < rows
                and 0 <= ncol < cols
                and visit[nrow][ncol] is False
                and heights[nrow][ncol] <= heights[row][col]
            ):
                can_steps.append((nrow, ncol))
        return any(self.dfs(nrow, ncol, rows, cols, heights, visit, border) for (nrow, ncol) in can_steps)


